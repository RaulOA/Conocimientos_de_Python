#Tipo de dato entero o largo(int)
datoentero=11
print(datoentero, type(datoentero))
print()

#Tipo de dato flotante (float)
datofloat=0.50
print(datofloat, type(datofloat))
print()

#Tipo de dato complejo (complex)
datocomplejo=5+6j
print(datocomplejo, type(datocomplejo))
print()

#Tipo de dato caracteres o cadenas (str)
datostr="Raul"
print(datostr, type(datostr))
print()

#Tipo de dato Booleano (bool)
num1=1
num2=2
datobool1=num1==num2
datobool2=num1!=num2
print(datobool1, type(datobool1))
print(datobool2, type(datobool2))